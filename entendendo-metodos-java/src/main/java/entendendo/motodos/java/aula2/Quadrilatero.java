package entendendo.motodos.java.aula2;

public class Quadrilatero {

    public double Area(double comprimento){
        return comprimento*comprimento;
    }

    public double Area(double comprimento, double largura){
        return comprimento*largura;
    }

    public double Area(double comprimento, double largura, double altura){
        return (comprimento+largura)*altura/2;
    }





}
